package com.company;

public class TabularOutput {
    public static void main(String[] args) {

        int n=1;

        System.out.println("N\t10*N\t100*N\t1000*N");

        while(n<=5){
            System.out.println(n+"\t"+10*n+"\t\t"+100*n+"\t\t"+1000*n);
            n++;

        }


    }
    }

